package manha;

public class Estudante {
    String nome;
    int idade;
    long numMatricula;
    String serie;

    Estudante(String nome, int idade, long numMatricula, String serie){
        this.nome = nome;
        this.idade = idade;
        this.numMatricula = numMatricula;
        this.serie = serie;
    }
}
