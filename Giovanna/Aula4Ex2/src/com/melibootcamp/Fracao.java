package com.melibootcamp;

public class Fracao {
    public double adicionar(double valor1, double valor2){
        return valor1+ valor2;
    }
    public double adicionar(double valor1, int valor2){
        return valor1+ valor2;
    }
    public double subtrair(double valor1, double valor2){
        return valor1 - valor2;
    }
    public double subtrair(double valor1, int valor2){
        return valor1 - valor2;
    }
    public double multiplicar(double valor1, double valor2){
     return valor1 * valor2;
    }
    public double multiplicar(double valor1, int valor2){
        return valor1 * valor2;
    }
public double dividir(double valor1, double valor2){
        return  valor1/valor2;
}
    public double dividir(double valor1, int valor2){
        return  valor1/valor2;
    }
}
